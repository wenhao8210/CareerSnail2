export const INTERVIEW_QUESTION_PROMPT = `你是一位专业的AI产品经理面试官。请根据以下简历内容和职位描述（JD），生成10道高质量的面试题。

简历内容：
{resume_content}

职位描述（JD）：
{jd_content}

要求：
1. 题目要结合简历中的具体项目经历和JD要求
2. 题目类型要多样化：项目深挖、技术理解、产品思维、场景设计、数据指标等
3. 每道题要提供精简、有条理的参考答案（100-150字）
4. 参考答案格式要求：
   - 使用分点或分段的方式，结构清晰
   - 可以使用"1. 2. 3."或"- - -"等格式
   - 重点突出，避免冗余描述
   - 体现AI产品经理的核心能力：技术理解、产品思维、数据驱动、用户洞察

**重要：你必须严格按照以下JSON格式输出，不要有任何其他文字、说明或markdown代码块标记，直接输出纯JSON：**

{
  "questions": [
    {
      "question": "问题内容",
      "answer": "精简有条理的参考答案（100-150字，使用分点格式）",
      "category": "问题类型（如：项目深挖/技术理解/产品思维/场景设计/数据指标）"
    }
  ]
}

请确保：
1. 输出的是有效的JSON格式
2. 包含10道题目
3. 每道题都有question、answer、category三个字段
4. answer字段内容要精简（100-150字），使用分点格式，条理清晰`;

export function generatePrompt(resumeContent: string, jdContent: string): string {
  if (!resumeContent?.trim()) throw new Error("简历内容不能为空");
  if (!jdContent?.trim()) throw new Error("JD 内容不能为空");
  return INTERVIEW_QUESTION_PROMPT
    .replace("{resume_content}", resumeContent.trim())
    .replace("{jd_content}", jdContent.trim());
}

